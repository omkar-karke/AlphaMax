int heuristic(char board[8][8])
{
    int value =0;

    for (int i = 0; i<8; i++)
    {
        for (int j = 0; j<8; j++)
        {
            if(board[i][j] == 'p')
            {
                value += 100;
                value+=pawn_square_table_Maximizer[i][j];
            }

            else if(board[i][j] == 'r')
            {
                value += 500;
                value+=rook_square_table_Maximizer[i][j];
            }

            else if(board[i][j] == 'n')
            {
                value += 320;
                value+=knight_square_table_Maximizer[i][j];
            }

            else if(board[i][j] == 'b')
            {
                value += 330;
                value+=bishop_square_table_Maximizer[i][j];
            }

            else if(board[i][j] == 'q')
            {
                value += 900;
                value+=queen_square_table_Maximizer[i][j];
            }

            else if(board[i][j] == 'k')
            {
                value += 20000;
                value+=king_square_table_Maximizer[i][j];
            }






            else if(board[i][j] == 'R')
            {
                value -= 500;
                value+=rook_square_table_Minimizer[i][j];
            }

            else if(board[i][j] == 'P')
            {
                value -= 100;
                value+=pawn_square_table_Minimizer[i][j];
            }

            else if(board[i][j] == 'N')
            {
                value -= 320;
                value+=knight_square_table_Minimizer[i][j];
            }

            else if(board[i][j] == 'B')
            {
                value -= 330;
                value+=bishop_square_table_Minimizer[i][j];
            }

            else if(board[i][j] == 'Q')
            {
                value -= 900;
                value+=queen_square_table_Minimizer[i][j];
            }

            else if(board[i][j] == 'K')
            {
                value -= 20000;
                value+=king_square_table_Minimizer[i][j];
            }
        }
    }

    return value;
}
